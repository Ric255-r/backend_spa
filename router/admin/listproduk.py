app = APIRouter(prefix=("/listproduk"))

@app.get('/getdataproduk')
async def getdataproduk() :
  try :
    pool = await get_db()

    async with pool.acquire() as conn:
      async with conn.cursor() as cursor:
        await cursor.execute("SET SESSION TRANSACTION ISOLATION LEVEL READ COMMITTED;")
        # await cursor.execute("COMMIT;")

        q1 = "SELECT * FROM menu_produk ORDER BY id_produk ASC"

        await cursor.execute(q1)  

        items = await cursor.fetchall()

        kolom_menu = [kolom[0] for kolom in cursor.description]
        df = pd.DataFrame(items, columns=kolom_menu)

        # print(" Final fetched items:", items)
        return df.to_dict('records')
  except HTTPException as e:
   return JSONResponse({"Error": str(e)}, status_code=e.status_code)


@app.put('/updateproduk')
async def updateproduk(
  request: Request
):
  try:
    pool = await get_db()

    async with pool.acquire() as conn:
      async with conn.cursor() as cursor:
        try:
          # 1. Start Transaction
          await conn.begin()

          # 2. Execute querynya
          data = await request.json()
          q1 = "UPDATE menu_produk SET nama_produk = %s, harga_produk = %s, stok_produk = %s, durasi = %s, tipe_komisi = %s, nominal_komisi = %s, tipe_komisi_gro = %s, nominal_komisi_gro = %s  WHERE id_produk= %s"
          await cursor.execute(q1, (data['nama_produk'],data['harga_produk'],data['stok_produk'],data['durasi'],data['tipe_komisi'],data['nominal_komisi'],data['tipe_komisi_gro'],data['nominal_komisi_gro'],data['id_produk']))
          # 3. Klo Sukses, dia bkl save ke db
          await conn.commit()

          return "succes"
        except aiomysqlerror.MySQLError as e:
          # Rollback Input Jika Error

          # Ambil Error code
          error_code = e.args[0] if e.args else "Unknown"
          
          await conn.rollback()
          return JSONResponse(content={"status": "Error", "message": f"Database Error{e} "}, status_code=500)
        
        except Exception as e:
          await conn.rollback()
          print(f"Error during insert : {e}")
          return JSONResponse(content={"status": "Error", "message": f"Server Error {e} "}, status_code=500)
        
  except Exception as e:
    return JSONResponse(content={"status": "Error", "message": f"Koneksi Error {str(e)}"}, status_code=500)
  
